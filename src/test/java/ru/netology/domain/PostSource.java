package ru.netology.domain;

public class PostSource {
    private String typePostSource;
    private String platformSource;
    private String dataSource;
    private String urlSource;

    public String getTypePostSource() {
        return typePostSource;
    }

    public void setTypePostSource(String typePostSource) {
        this.typePostSource = typePostSource;
    }

    public String getPlatformSource() {
        return platformSource;
    }

    public void setPlatformSource(String platformSource) {
        this.platformSource = platformSource;
    }

    public String getDataSource() {
        return dataSource;
    }

    public void setDataSource(String dataSource) {
        this.dataSource = dataSource;
    }

    public String getUrlSource() {
        return urlSource;
    }

    public void setUrlSource(String urlSource) {
        this.urlSource = urlSource;
    }
}
